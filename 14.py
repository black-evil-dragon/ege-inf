# yAAx 12 + x02y 14 80

array = []
for x in '0123456789AB':
    for y in '0123456789AB':
        t = int(y + 'AA' + x, 12) + int(x + '02' + y, 14)
        if t % 80 == 0:
            array.append(t)

if array:
    print(min(array) // 80)


# 88x4y 9 + 7x44y 11
# наименьшее и кратно 61
# вычислите частное от деления значения арифметического выражения на

array = []
for x in range(9):
    for y in range(9):
        t = int('88' + str(x) + '4' + str(y), 9) + int('7' + str(x) + '44' + str(y), 11)
        if t % 61 == 0:
            array.append(t)

if array:
    print(min(array) // 61)


